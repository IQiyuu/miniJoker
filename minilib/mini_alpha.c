#include "minilib.h"

int mini_alpha(char c)
{
    return ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'));
}

int mini_alphanum(char c)
{
    return (mini_alpha(c) || (c >= '0' && c <= '9'));
}
