#include "minilib.h"

void    mini_stradd(char *s1, char *toadd)
{
    int i;
    int j;

    j = 0;
    i = 0;
    while (s1[i])
        i++;
    while (toadd[j])
        s1[i++] = toadd[j++];
    s1[i] = '\0';
}